#include "mozMath.h"

namespace moz
{
	namespace math
	{
		//------------------------------------------------------------------------------
		// コンスラクタ
		//------------------------------------------------------------------------------
		Vector4D::Vector4D(float _x, float _y, float _z, float _w)
		{
			v.x = _x;
			v.y = _y;
			v.z = _z;
			v.w = _w;
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍
		//------------------------------------------------------------------------------
		Vector4D Vector4D::operator*(float f) const
		{
			return Vector4D(v.x*f, v.y*f, v.z*f, v.w*f);
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍
		//------------------------------------------------------------------------------
		Vector4D Vector4D::operator/(float f) const
		{
			// 逆数とって計算したほうが早い
			float fInv = 1.f / f;
			return Vector4D(v.x*fInv, v.y*fInv, v.z*fInv, v.w*fInv);
		}

		//------------------------------------------------------------------------------
		// ベクタ同士の足し算
		//------------------------------------------------------------------------------
		Vector4D Vector4D::operator+(const Vector4D& _v) const
		{
			return Vector4D(v.x + _v.v.x, v.y + _v.v.y, v.z + _v.v.z, v.w + _v.v.w);
		}

		//------------------------------------------------------------------------------
		// ベクタ同士の差
		//------------------------------------------------------------------------------
		Vector4D Vector4D::operator-(const Vector4D& _v) const
		{
			return Vector4D(v.x - _v.v.x, v.y - _v.v.y, v.z - _v.v.z, v.w - _v.v.w);
		}

		//------------------------------------------------------------------------------
		// ベクタの前に書かれてた+記号
		//------------------------------------------------------------------------------
		Vector4D Vector4D::operator+() const
		{
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタの前に書かれてた-記号
		//------------------------------------------------------------------------------
		Vector4D Vector4D::operator-() const
		{
			return Vector4D(-v.x, -v.y, -v.z, -v.w);
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍の代入
		//------------------------------------------------------------------------------
		Vector4D& Vector4D::operator*=(float f)
		{
			v.x *= f;
			v.y *= f;
			v.z *= f;
			v.w *= f;
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍の代入
		//------------------------------------------------------------------------------
		Vector4D& Vector4D::operator/=(float f)
		{
			float fInv = 1.f / f;
			v.x *= fInv;
			v.y *= fInv;
			v.z *= fInv;
			v.w *= fInv;
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタの和と代入の合成
		//------------------------------------------------------------------------------
		Vector4D& Vector4D::operator+=(const Vector4D& _v)
		{
			v.x += _v.v.x;
			v.y += _v.v.y;
			v.z += _v.v.z;
			v.w += _v.v.w;
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタの和と代入の合成
		//------------------------------------------------------------------------------
		Vector4D& Vector4D::operator-=(const Vector4D& _v)
		{
			v.x -= _v.v.x;
			v.y -= _v.v.y;
			v.z -= _v.v.z;
			v.w -= _v.v.w;
			return *this;
		}
	}
}