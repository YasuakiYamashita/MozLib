#include "mozMath.h"

namespace moz
{
	namespace math
	{
		//------------------------------------------------------------------------------
		// コンスラクタ
		//------------------------------------------------------------------------------
		Vector3D::Vector3D(float _x, float _y, float _z)
		{
			v.x = _x;
			v.y = _y;
			v.z = _z;
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍
		//------------------------------------------------------------------------------
		Vector3D Vector3D::operator*(float f) const
		{
			return Vector3D(v.x*f, v.y*f, v.z*f);
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍
		//------------------------------------------------------------------------------
		Vector3D Vector3D::operator/(float f) const
		{
			// 逆数とって計算したほうが早い
			float fInv = 1.f / f;
			return Vector3D(v.x*fInv, v.y*fInv, v.z*fInv);
		}

		//------------------------------------------------------------------------------
		// ベクタ同士の足し算
		//------------------------------------------------------------------------------
		Vector3D Vector3D::operator+(const Vector3D& _v) const
		{
			return Vector3D(v.x + _v.v.x, v.y + _v.v.y, v.z + _v.v.z);
		}

		//------------------------------------------------------------------------------
		// ベクタ同士の差
		//------------------------------------------------------------------------------
		Vector3D Vector3D::operator-(const Vector3D& _v) const
		{
			return Vector3D(v.x - _v.v.x, v.y - _v.v.y, v.z - _v.v.z);
		}

		//------------------------------------------------------------------------------
		// ベクタの前に書かれてた+記号
		//------------------------------------------------------------------------------
		Vector3D Vector3D::operator+() const
		{
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタの前に書かれてた-記号
		//------------------------------------------------------------------------------
		Vector3D Vector3D::operator-() const
		{
			return Vector3D(-v.x, -v.y, -v.z);
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍の代入
		//------------------------------------------------------------------------------
		Vector3D& Vector3D::operator*=(float f)
		{
			v.x *= f;
			v.y *= f;
			v.z *= f;
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍の代入
		//------------------------------------------------------------------------------
		Vector3D& Vector3D::operator/=(float f)
		{
			float fInv = 1.f / f;
			v.x *= fInv;
			v.y *= fInv;
			v.z *= fInv;
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタの和と代入の合成
		//------------------------------------------------------------------------------
		Vector3D& Vector3D::operator+=(const Vector3D& _v)
		{
			v.x += _v.v.x;
			v.y += _v.v.y;
			v.z += _v.v.z;
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタの和と代入の合成
		//------------------------------------------------------------------------------
		Vector3D& Vector3D::operator-=(const Vector3D& _v)
		{
			v.x -= _v.v.x;
			v.y -= _v.v.y;
			v.z -= _v.v.z;
			return *this;
		}
	}
}