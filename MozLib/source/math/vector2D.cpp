#include "mozMath.h"

namespace moz
{
	namespace math
	{
		//------------------------------------------------------------------------------
		// コンスラクタ
		//------------------------------------------------------------------------------
		Vector2D::Vector2D(float _x, float _y)
		{
			v.x = _x;
			v.y = _y;
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍
		//------------------------------------------------------------------------------
		Vector2D Vector2D::operator*(float f) const
		{
			return Vector2D(v.x*f, v.y*f);
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍
		//------------------------------------------------------------------------------
		Vector2D Vector2D::operator/(float f) const
		{
			// 逆数とって計算したほうが早い
			float fInv = 1.f / f;
			return Vector2D(v.x*fInv, v.y*fInv);
		}

		//------------------------------------------------------------------------------
		// ベクタ同士の足し算
		//------------------------------------------------------------------------------
		Vector2D Vector2D::operator+(const Vector2D& _v) const
		{
			return Vector2D(v.x + _v.v.x, v.y + _v.v.y);
		}

		//------------------------------------------------------------------------------
		// ベクタ同士の差
		//------------------------------------------------------------------------------
		Vector2D Vector2D::operator-(const Vector2D& _v) const
		{
			return Vector2D(v.x - _v.v.x, v.y - _v.v.y);
		}

		//------------------------------------------------------------------------------
		// ベクタの前に書かれてた+記号
		//------------------------------------------------------------------------------
		Vector2D Vector2D::operator+() const
		{
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタの前に書かれてた-記号
		//------------------------------------------------------------------------------
		Vector2D Vector2D::operator-() const
		{
			return Vector2D(-v.x, -v.y);
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍の代入
		//------------------------------------------------------------------------------
		Vector2D& Vector2D::operator*=(float f)
		{
			v.x *= f;
			v.y *= f;
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタスカラ倍の代入
		//------------------------------------------------------------------------------
		Vector2D& Vector2D::operator/=(float f)
		{
			float fInv = 1.f / f;
			v.x *= fInv;
			v.y *= fInv;
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタの和と代入の合成
		//------------------------------------------------------------------------------
		Vector2D& Vector2D::operator+=(const Vector2D& _v)
		{
			v.x += _v.v.x;
			v.y += _v.v.y;
			return *this;
		}

		//------------------------------------------------------------------------------
		// ベクタの和と代入の合成
		//------------------------------------------------------------------------------
		Vector2D& Vector2D::operator-=(const Vector2D& _v)
		{
			v.x -= _v.v.x;
			v.y -= _v.v.y;
			return *this;
		}
	}
}